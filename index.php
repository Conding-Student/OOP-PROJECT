							<h4>Bundok ng Amuyaw</h4>
							<p style="margin: 10px 2px 15px 15px;">Isang bundok na namumukod-tanging ganda ng kalikasan, may bukirin at mga lambak na luntian at sagana sa buhay. Tinatampok ng mga ilog at bukal na nagbibigay ng buhay sa paligid. Ito rin ang naging tahanan nina Bugan at Wigan pagkatapos ng unang trahedya.</p>

							
							<h4>Bundok ng Kalawitan</h4>
							<p style="margin: 15px 2px 15px 15px;">Isang mapanganib na bundok na puno ng misteryo at pagkaalipin. Mga matarik na bangin at batuhan ang laging dinadalaw ng makulimlim na ulap. Kilala ito sa kanyang malakas na hampas ng hangin at matatarik na tanawin, nagbibigay ng hamon at panganib sa mga naglalakbay.</p>

							<h4>Mga ilog at kagubatan ng Ifugao</h4>
							<p style="margin: 15px 2px 15px 15px;">Noon, ang mga ilog ay umaagos ng malinaw na tubig na puno ng isda, habang ang kagubatan ay sagana sa mga puno at hayop. Subalit, sa gitna ng trahedya, ang mga ilog ay natuyo at ang mga kagubatan ay nawasak, nagdulot ng pangamba at kahirapan sa mga mamamayan ng Ifugao.</p>

							<h4>Mga natabing nayon ng mga Ifugao</h4>
							<p style="margin: 15px 2px 15px 15px;">Ang mga nayon ng mga Ifugao ay dating mga palaisdaan ng kasayahan at aktibidad. Subalit matapos ang trahedya, naging lugar ito ng pagluluksa at pagkalugmok. Ang mga tahanan ay naging guho, at ang dating mga taniman ay naging tigib sa kapahamakan at pangamba.</p>

